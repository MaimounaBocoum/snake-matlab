    function LoadSeq(source, eventdata,hf)
        % If the window is not closed each time, loadSEQ creates new
        % buttons over the old ones every time it is launched making the
        % code slower and slower. AO.hlOpen is a marker stating if the
        % window is closed or open so that the initialization of the
        % buttons only occurs when necessary
        
        AO = guidata(hf);
        
        if findobj('type','figure','tag','hl')
            AO.hlOpen = 1;
            figure(AO.hl)
        else
            % General figure for the load sequence GUI
            hl = figure('Visible','on',...
                'units','normalized',...
                'menubar','none',...
                'NumberTitle','off',...
                'Name','Load US Sequence',...
                'OuterPosition',[0.5 0.05 0.3 0.9],...
                'tag','hl');
            
            AO.hl = hl;
            AO.hlOpen = 0;
        end
        
        guidata(hf,AO); % save parameters data
        
        loadSEQ(hf);
        
    end

